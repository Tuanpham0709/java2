    public boolean checkDate(String ngaysinh) {
        try {
            String date = "dd/MM/yyyy";
            SimpleDateFormat format = new SimpleDateFormat(date);
            format.parse(ngaysinh);
        } catch (Exception e) {

            return false;
        }

        return true;
    }

    public boolean checkPoint() {
        if (Double.parseDouble(tf_diem.getText()) < 0 || Double.parseDouble(tf_diem.getText()) > 10) {
            return false;
        }
        return true;
    }

    public ArrayList<Student> readFile(String path) {
        ArrayList<Student> list = new ArrayList<>();
        try {
            ObjectInputStream ois = new ObjectInputStream(new FileInputStream(path));
            list = (ArrayList<Student>) ois.readObject();
        } catch (Exception e) {
            e.printStackTrace();
            return null;
        }
        return list;
    }

