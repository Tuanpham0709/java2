    private void btn_ghiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ghiActionPerformed
        // TODO add your handling code here:
        if (checkEmptyString()) {
            try {
                if (!checkPoint()) {
                    JOptionPane.showMessageDialog(this, "Diem phai tu 0 den 10");
                    return;
                } else if (!checkDate(tf_ngaysinh.getText())) {
                    JOptionPane.showMessageDialog(this, "Ban phai nhap dung dinh dan ngay sinh");
                    return;
                } else {
                    addStudent();
                    filltoTable();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Diem nhap vao phai la so");
            }
        } else {
            JOptionPane.showMessageDialog(this, "khong de trong cac o nhap");
        }
    }//GEN-LAST:event_btn_ghiActionPerformed

