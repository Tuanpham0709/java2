    public void addStudent() {
        Student student = new Student();
        student.setName(tf_nane.getText());
        student.setNganhhoc(cbb_nganhhoc.getSelectedItem().toString());
        student.setNgaysinh(tf_ngaysinh.getText());
        student.setDiem(tf_diem.getText());
        listStudent.add(student);
        writeFile(path, listStudent);
    }

    public void writeFile(String path, ArrayList<Student> list) {
        try {
            ObjectOutputStream oos = new ObjectOutputStream(new FileOutputStream(path));
            oos.writeObject(list);
            oos.close();
        } catch (Exception e) {
        }
    }

    public void filltoTable() {
        model.setRowCount(0);
        ArrayList<Student> list = readFile(path);
        for (Student s : list) {
            model.addRow(new Object[]{s.getName(), s.getNganhhoc(), s.getDiem(), s.getNgaysinh()});
        }
    }

    public boolean checkEmptyString() {
        if (tf_nane.getText().length() == 0 || tf_diem.getText().length() == 0 || tf_ngaysinh.getText().length() == 0) {
            return false;
        }
        return true;
    }

